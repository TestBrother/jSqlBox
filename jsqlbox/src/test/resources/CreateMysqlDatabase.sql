(id integer auto_increment ,
constraint const1 primary key (ID),
username Varchar (50) ,
PhoneNumber Varchar (50) ,
Address Varchar (50) ,
Alive Boolean, 
Age Integer )ENGINE=InnoDB DEFAULT CHARSET=utf8;
